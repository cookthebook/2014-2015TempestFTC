#include "JoystickDriver.c"
#include "drivers/hitechnic-sensormux.h"
#include "drivers/hitechnic-irseeker-v2.h"
#include "drivers/hitechnic-gyro.h"

#define SeekerL 	msensor_S3_1
#define SeekerR 	msensor_S3_2
#define Gyro			msensor_S3_3

int speed = 30;

void Right(const int power){
	motor[Right1] = power;
	motor[Right2] = power;
}

void Left(const int power){
	motor[Left1] = power;
	motor[Left2] = power;
}

task main()
{
HTGYROstartCal(Gyro);
bool Triangulating = true;
while(Triangulating){
	if(HTIRS2readDCDir(SeekerL) < 6){
	if(HTIRS2readDCDir(SeekerR) < 4){
		//left
		Right(speed);
		Left(-speed);
	}

	if(HTIRS2readDCDir(SeekerR) > 4){
		//forward
		Right(speed);
		Left(speed);
	}

	if(HTIRS2readDCDir(SeekerR) == 4){
		//left
		Right(speed);
		Left(-speed);
	}
	}



	if(HTIRS2readDCDir(SeekerL) > 6){
	if(HTIRS2readDCDir(SeekerR) < 4){
		//back
		Right(-speed);
		Left(-speed);
	}

	if(HTIRS2readDCDir(SeekerR) > 4){
		//right
		Right(-speed);
		Left(speed);
	}

	if(HTIRS2readDCDir(SeekerR) == 4){
		//right
		Right(-speed);
		Left(speed);
	}
	}



	if(HTIRS2readDCDir(SeekerL) == 6){
	if(HTIRS2readDCDir(SeekerR) < 4){
		//left
		Right(-speed);
		Left(speed);
	}

	if(HTIRS2readDCDir(SeekerR) > 4){
		//right
		Right(-speed);
		Left(speed);
	}

	if(HTIRS2readDCDir(SeekerR) == 4){
		//stop, break
		Right(0);
		Left(0);
		Triangulating = false;
	}
	}
}
}
