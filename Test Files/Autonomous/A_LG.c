void triangulate(){
bool Triangulating = true;

while(Triangulating){
	if(HTIRS2readDCDir(SeekerL) < 6){
	if(HTIRS2readDCDir(SeekerR) < 4){
		//left
		Right(mSpeed / 2);
		Left(-mSpeed / 2);
	}

	if(HTIRS2readDCDir(SeekerR) > 4){
		//forward
		Right(mSpeed / 2);
		Left(mSpeed / 2);
	}

	if(HTIRS2readDCDir(SeekerR) == 4){
		//left
		Right(mSpeed / 2);
		Left(-mSpeed / 2);
	}
	}



	if(HTIRS2readDCDir(SeekerL) > 6){
	if(HTIRS2readDCDir(SeekerR) < 4){
		//back
		Right(-mSpeed / 2);
		Left(-mSpeed / 2);
	}

	if(HTIRS2readDCDir(SeekerR) > 4){
		//right
		Right(-mSpeed / 2);
		Left(mSpeed / 2);
	}

	if(HTIRS2readDCDir(SeekerR) == 4){
		//right
		Right(-mSpeed / 2);
		Left(mSpeed / 2);
	}
	}



	if(HTIRS2readDCDir(SeekerL) == 6){
	if(HTIRS2readDCDir(SeekerR) < 4){
		//left
		Right(-mSpeed / 2);
		Left(mSpeed / 2);
	}

	if(HTIRS2readDCDir(SeekerR) > 4){
		//right
		Right(-mSpeed / 2);
		Left(mSpeed / 2);
	}

	if(HTIRS2readDCDir(SeekerR) == 4){
		//stop, break
		Right(0);
		Left(0);
		Triangulating = false;
	}
	}
}

trans = 4;
}



