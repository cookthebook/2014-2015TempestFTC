float trans = 0;
int mSpeed = 50;
float tooSlow = 0.2;

float RPM(tMotor input){
	int position1;
	int position2;
	float rpm;

	wait1Msec(100);
	position1 = nMotorEncoder(input);
	wait1Msec(100);
	position2 = nMotorEncoder(input);

	rpm = abs(((float)position2 - (float)position1) * 600 / 1440);//converting from ticks/decisecond to rotations/second

	return rpm;
}



void Right(int speed){
	motor[Right1] = speed;
	motor[Right2] = speed;
}

void Left(int speed){
	motor[Left1] = speed;
	motor[Left2] = speed;
}



void forward(const int distance){
	nMotorEncoder(Right1) = 0;
	nMotorEncoder(Left1) = 0;

	Right(mSpeed);
	Left(mSpeed);

	ClearTimer(T2);

	while(nMotorEncoder(Right1) < distance && nMotorEncoder(Left1) < distance){
		//Keep straight
		if(HTGYROreadCal(Gyro) < -5){
			Right(mSpeed + 1);
			Left(mSpeed - 1);
		}
		else if(HTGYROreadCal(Gyro) > 5){
			Right(mSpeed - 1);
			Left(mSpeed + 1);
		}
		else{
			Right(mSpeed);
			Left(mSpeed);
		}

		//Obstacle
		if(RPM(Right1) < tooSlow && time1(T2) > 2000){
			Right(0);
			Left(0);
			wait1Msec(2000);
			Right(mSpeed);
			Left(mSpeed);
			ClearTimer(T2);
		}
		if(RPM(Left1) < tooSlow && time1(T2) > 2000){
			Right(0);
			Left(0);
			wait1Msec(2000);
			Right(mSpeed);
			Left(mSpeed);
			ClearTimer(T2);
		}
	}

	Right(0);
	Left(0);

	trans = 1;
}



void findPos(){
	if(HTIRS2readDCDir(SeekerR) == 5 && HTIRS2readDCDir(SeekerL) == 5) trans = 2.1;

	if(HTIRS2readDCDir(SeekerR) == -1 && HTIRS2readDCDir(SeekerL) == -1) trans = 2.2;

	if(HTIRS2readDCDir(SeekerR) == 3 && HTIRS2readDCDir(SeekerL) == 3) trans = 2.3;
}



